      <h2>摘　要</h2>
      <p>高质量发展背景下，人民对美好生活的向往日益强烈，增进人民幸福逐渐成为社会关注的重大议题。使用2018年中国家庭追踪调查（CFPS），实证分析个体慈善捐赠行为对其主观幸福感的影响及作用机制。研究发现，慈善捐赠行为对居民幸福感具有正向且稳健的提升作用。机制分析发现，提升社会关联能力、提高社会地位以及增加积极情绪是慈善捐赠行为影响居民幸福感的重要渠道。异质性分析发现，慈善捐赠行为对居民幸福感的积极作用在高收入满意度人群、城镇人群、低学历人群、中年人群，以及北方和西部地区样本中表现更为明显。研究结果为当下增进人民福祉、不断实现人民群众对美好生活的向往提供了新的思路和见解。</p>
      <p class="keywords"><strong>关键词：</strong>慈善捐赠行为；主观幸福感；社会关联能力；社会地位；积极情绪</p>
