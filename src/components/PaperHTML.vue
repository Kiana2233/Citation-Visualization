      <section class="introduction">
        <h2>引言</h2>
        <p>幸福感是人民美好生活需要的重要内容和整体表现。党的十八大以来，以习近平同志为核心的党中央高度重视民生建设，始终坚持以人民为中心的发展思想，围绕"必须在发展中保障和改善民生，鼓励共同奋斗创造美好生活，不断实现人民对美好生活的向往"。提出了一系列新理念、新部署、新要求。2023年，习近平总书记在参加十四届全国人大一次会议江苏代表团审议时再次强调，"必须以满足人民日益增长的美好生活需要为出发点和落脚点，不断增强人民群众的获得感、幸福感、安全感"。这一重要论述深刻揭示了推动高质量发展的价值追求，为不断实现人民对美好生活的向往指明了方向。然而，历年《全球幸福指数报告》显示，中国居民的幸福指数排名虽然逐年上升，但是从世界范围比较来看，仍具有很大的提升空间<sup>①</sup>。因此，新发展阶段、新发展理念、新发展格局背景下，如何进一步增进人民福祉，增强人民群众幸福感，仍是当下推进高质量发展需要重点思考的问题。</p>
        <p>早期有关个体主观幸福感的研究较多集中于心理学、哲学和社会学视域<sup>[1-2]</sup>。21世纪以来，随着各个国家和地区政府将居民幸福作为评判政府工作的重要指标，经济学家们开始运用经济学分析工具，并结合社会学、心理学、伦理学等诸多学科，对影响个体主观幸福感的因素进行了广泛探索<sup>[3]</sup>。相关文献可大致分为3类。第一，宏观层面经济波动与经济政策的影响<sup>[1]</sup>，如经济衰退与通货膨胀<sup>[4-5]</sup>、经济增长<sup>[6]</sup>、收入分配政策和财政政策<sup>[7-8]</sup>。第二，外部自然和社会环境因素的作用，比如空气污染<sup>[9]</sup>、社会网络<sup>[10]</sup>、社会资本<sup>[11]</sup>、机会不均<sup>[12]</sup>、社会融合<sup>[13]</sup>，以及互联网使用等因素<sup>[3]</sup>。第三，个体层面的人口学特征和个体行为。人口学特征包括年龄、性别与健康<sup>[1,2,14]</sup>、婚姻和生育<sup>[15-16]</sup>、教育状况<sup>[17]</sup>、外貌<sup>[18]</sup>等。个体行为因素包括就业与创业<sup>[19-20]</sup>、收入和消费<sup>[21-23]</sup>等。</p>
        <p>尽管学术界已有诸多研究对个体幸福感影响因素展开探索和分析，但仍有一个尚未充分探讨的领域，即个体利他主义行为是如何影响其主观幸福感的？Becker<sup>[24]</sup>于1974年提出了纯利他主义（purely altruistic），并将其模型化。该模型认为个体进行慈善捐赠实际上是出于效用最大化的理性选择，而个体的效用不仅取决于自己的消费水平，还取决于他人的福利水平。随着利他主义理论的发展，学术界对利他行为的研究视角也从纯粹的自利动机转向了非纯利他动机。Andreoni<sup>[25-26]</sup>从非纯利他动机（impurely altruistic motive）视角提出了"warm-glow"理论（温情效应）。该理论认为，人们除了能从他人捐赠行为中获得社会福利以外，也能从自身慈善捐赠行为中获得效用。换言之，捐赠者通过自身的捐赠行为获得乐趣和满足感，而不是Becker<sup>[24]</sup>模型中隐含的捐赠者的快乐和幸福感来自于受捐赠者的快乐。Roseman<sup>[27]</sup>进一步将"温情效应"所引发的情绪反应归结为快乐、愉悦等积极的情感体验。因此，慈善捐赠行为在理论层面可以对个体幸福感产生重要影响。</p>
        <p>基于此，本文利用2018年中国家庭追踪调查（CFPS），重点探讨"温情效应"理论下个人慈善捐赠行为如何影响其主观幸福感。本文研究发现，居民慈善捐赠行为可以显著提高其个人主观幸福感，这一结果在经过一系列稳健性检验之后依然成立。进一步的机制分析发现，个体慈善捐赠行为通过提升其社会关联能力、提高其社会地位，以及增加积极情绪对其主观幸福感产生积极影响。异质性分析表明，个体慈善捐赠行为对幸福感的提升作用在高收入满意度人群、城镇人群、低学历人群、中年人群，以及北方和西部地区样本中的表现更为显著。</p>
        <p>本文的主要贡献在于：第一，基于经济学经典"非纯利他主义"理论，考察了个体慈善捐赠行为对其主观幸福感的影响，是对现有幸福经济学影响因素研究的有益补充。第二，从理论层面提出了居民慈善捐赠行为影响其幸福感的主要机制，并基于中国家庭追踪调查数据对相关机制进行了实证检验。第三，从分收入、分户籍、分学历、分年龄、分区域的角度对个体慈善捐赠对其幸福感的影响进行分样本回归，探讨了不同群体慈善捐赠行为对其幸福感影响的异质性。</p>
        <p>本文的结构安排如下：第一部分是理论基础与研究假说；第二部分是数据与变量说明以及模型构造；第三部分展示了基准回归、稳健性检验以及异质性分析的实证结果；第四部分检验了居民慈善捐赠行为影响其幸福感的传导机制；第五部分为本文的主要结论、研究不足以及进一步的研究方向。</p>
      </section>

      <section class="hypotheses">
        <h2>一、研究假说</h2>
        
        <h3>（一）"温情效应"理论模型</h3>
        <p>根据Becker<sup>[24]</sup>的纯利他主义和Andreoni<sup>[25-26]</sup>非纯利他主义，本文建立以下模型：假定消费者效用来源于两方面，一是个人私人物品的消费，二是公共物品的消费，其中私人物品可以转化为公共物品。假定个人财富为W<sub>i</sub>，个人消费为X<sub>i</sub>，社会总人数为N，个人慈善捐赠为g<sub>i</sub>，公共物品不包含来自政府的公共支出，则社会公共物品数量由个人捐赠和他人捐赠构成，可表示为：</p>
        <p class="formula">G = ∑<sub>i=1</sub><sup>N</sup> g<sub>i</sub></p>
        <p>个人效用函数为U<sub>i</sub> = U<sub>i</sub> (X<sub>i</sub>, G, g<sub>i</sub>)，其中i=1,2,3,...,N。假定U<sub>i</sub>是严格凹的，g<sub>i</sub>在消费者效用函数中出现了两次，其中一次是作为公共物品G<sub>i</sub>出现，一次是作为私人物品g<sub>i</sub>出现。以下分为两种情况讨论消费者效用函数。</p>
        <p>（1）当个人不关心慈善捐赠时，消费者效用函数转化为U<sub>i</sub> = U<sub>i</sub> (X<sub>i</sub>, G)，消费者效用完全来自于个人私人消费X<sub>i</sub>，以及公共物品G（该公共物品完全来自于他人的慈善捐赠）。第一种效用模型就是Becker的纯利他主义。</p>
        <p>（2）如果消费者考虑G和g时，则个人是非纯利他主义，即"温情效应"模型。假设在"温情效应"模型中他人捐赠给公共物品G<sub>j</sub>为一个非负的数量。除个人以外其他人的慈善捐赠为：</p>
        <p class="formula">G<sub>j</sub> = ∑<sub>j≠i</sub><sup>N-1</sup> g<sub>i</sub></p>
        <p>保留纳什均衡策略，消费者效用最大化求解可以写成如下方程：</p>
        <p class="formula">max U<sub>i</sub> (X<sub>i</sub>, G, g<sub>i</sub>)</p>
        <p class="formula">s.t. X<sub>i</sub> + g<sub>i</sub> = w<sub>i</sub>; G<sub>j</sub> + g<sub>i</sub> = G</p>
        <p>在假定纳什均衡前提下，G<sub>j</sub>是完全外生的变量，因此将g<sub>i</sub> = G - G<sub>j</sub>带入消费者效用函数可以得到如下方程：</p>
        <p class="formula">max<sub>G</sub> U<sub>i</sub> (w<sub>i</sub> + G<sub>j</sub> - G; G; G - G<sub>j</sub>)</p>
        <p>从以上方程看，消费者i的效用来源于3个方面：私人消费w<sub>i</sub> + G<sub>j</sub> - G；全社会公共物品总供给的非竞争性利益G；自身慈善捐赠G - G<sub>j</sub>，这部分就是个人捐赠带来的"温情效应"。</p>
        <p>根据"温情效应"模型，慈善捐赠行为不仅展现了捐赠者的价值观，同时还对增加捐赠者愉悦、提高生活满意度和积极情绪等方面起着重要作用。其一，"温情效应"模型得到了神经生理学研究的支持。有研究发现，慈善捐赠行为能够激活涉及奖赏体验的区域，如眼眶额叶皮质和腹侧纹状体<sup>[28-30]</sup>。其二，捐赠行为可以通过向下的社会比较和自尊心两种路径影响捐赠者的生活满意度<sup>[31]</sup>。其三，捐赠行为有助于提高捐赠者心理健康<sup>[32]</sup>，降低绝望感<sup>[33]</sup>。其四，从中国传统儒家"仁"的思想来看，慈善捐赠也被认为是个体价值实现的重要途径之一<sup>[34]</sup>，并因此对个体主观幸福感产生重要影响。</p>
        <p class="hypothesis">基于此，本文提出假说H1：个体慈善捐赠行为能够提高其主观幸福感。</p>

        <h3>（二）社会关联方面</h3>
        <p>慈善捐赠行为对社会关联的影响：慈善捐赠是提高个体社会关联程度的重要途径。捐赠行为的本质是一种自发的人与人之间的互动。一方面，这种自发行为可以降低人的社会隔离感，并增强人们的社会关联感、信任感与认同感<sup>[35-36]</sup>；另一方面，除了增强正面主观知觉以外，慈善捐赠行为也可以促进与他人的社会交流，扩大社会网络并增强社会凝聚力<sup>[37]</sup>。因此，捐赠者能从自己参与的捐赠活动中与伙伴、社会建立更广泛的联系。</p>
        <p>社会关联对个体主观幸福感的影响：作为社会参与和社会资本的体现，个体社会关联程度对自身幸福感在很大程度上具有决定作用。其一，中国是一个典型的关系型社会，积极的社会联系可以提升居民的社会参与，从而对其幸福感有正向效应<sup>[10]</sup>。尤其对于边缘化的群体而言，个体所感知到的社会参与感、社会归属感对于提升其幸福感至关重要。其二，社会关联程度是个体社会资本积累程度的体现。更广泛、更多元的社会网络是个体生产生活所需社会资源的重要来源，并由此增强个体的满足感<sup>[38]</sup>。</p>
        <p class="hypothesis">基于此，本文提出假说H2：慈善捐赠行为通过提高个体的社会关联程度增强其主观幸福感。</p>

        <h3>（三）社会地位方面</h3>
        <p>慈善捐赠行为对社会地位的影响：高成本信号理论认为高成本的利他行为可靠地传递出关于个人品质的信号<sup>[39]</sup>，进而对利他者的群体地位产生显著影响。一方面，利他行为可以增加实施者的适应性，个体通过捐赠行为向他人传递个人品质、影响力和吸引力<sup>[40]</sup>，增加他人对捐赠者的信任和依赖，进而对捐赠者的群体内地位具有"向上流动"的作用<sup>[39,41]</sup>。另一方面，个体通过捐赠行为可以获得更高的社会评价以及群体认可和尊敬，并且人们更愿意选择捐赠者作为群体领导<sup>[42]</sup>，进而提高其群体内地位<sup>[43]</sup>。</p>
        <p>社会地位对幸福感的影响：社会地位在一定程度上可以影响其幸福。一方面，社会地位更高的个体在与社会地位较低群体进行下行比较时，会获得"知足"的相对满足感和愉悦感，有利于提升幸福感<sup>[44]</sup>。另一方面，社会地位更高的群体通常可以获得更高的安全感以及社会支持等社会资源，对幸福有积极的提升作用<sup>[45-46]</sup>。</p>
        <p class="hypothesis">基于此，本文提出假说H3：慈善捐赠行为通过提升个体的社会地位增强其主观幸福感。</p>

        <h3>（四）积极情绪方面</h3>
        <p>慈善捐赠行为对个体积极情绪的影响：根据积极情绪拓展构建理论，个体可以从社会活动中获得积极的情绪体验<sup>[47]</sup>。具体而言，慈善捐赠行为作为一种自发的利他行为，可以缓解负面情绪，比如压力情绪<sup>[48]</sup>。在相关利他行为的实证研究方面，有学者认为慈善捐赠等利他行为满足了人们对于关系和能力的需求，增强了自我评价、产生了积极情绪<sup>[49]</sup>。此外，给予他人帮助往往伴随着改变他人的生活，如被帮助者表达感激之情，可以进一步促进个体积极情绪的产生<sup>[50]</sup>。</p>
        <p>积极情绪对幸福感的影响：积极情绪主要包括自信心、乐观、开朗等一系列影响居民幸福感的内部个体因素<sup>[1]</sup>。研究表明，伴随着压力等负面情绪的产生，个体患病率增高，其幸福感降低，并导致自杀率提高<sup>[51]</sup>。然而，积极健康的情绪可以对幸福感有正向影响。积极情绪体验拓宽了个体认知范围，这有助于应对问题和构建资源，而积极的应对方式又能促进积极情绪的产生，这种螺旋式的上升过程有助于提升个体幸福感<sup>[52]</sup>。</p>
        <p class="hypothesis">基于此，本文提出假说H4：慈善捐赠行为通过促进个体积极情绪的产生增强其主观幸福感。</p>
      </section>

      <section class="research-design">
        <h2>二、研究设计</h2>
        
        <h3>（一）数据来源</h3>
        <p>本文的数据来源于北京大学2018年中国家庭追踪调查（CFPS）。该数据库通过对全国具有代表性的城乡家庭及其家庭成员的跨期跟踪调查，反映了中国的经济发展和社会变迁状况。本文使用CFPS数据进行实证研究的优点在于：第一，CFPS具非常丰富的家庭微观信息，包括个人基本特征、收入和就业状况等有可能会影响居民幸福感的因素。第二，CFPS的数据提供了家庭中个人的慈善捐赠行为以及对他人的关心等信息，可以有效地测度其对幸福感的影响作用。第三，样本代表性充足，该数据库采用多阶段PPS抽样，样本覆盖了全国25个省（自治区、直辖市），并且样本的城乡分布、年龄结构、性别比例与收入结构合理。在剔除缺失值后，本文共得到7471个有效样本。</p>

        <h3>（二）基准模型与中介模型</h3>
        <h4>1. 基准回归模型</h4>
        <p>为检验慈善捐赠行为是否可以对居民幸福感产生显著影响，本文设定基准回归模型如下：</p>
        <p class="formula">Happiness<sub>i</sub> = α<sub>0</sub> + α<sub>1</sub> DONA<sub>i</sub> + ΓX<sub>i</sub> + μ (1)</p>
        <p>式（1）中的被解释变量Happiness<sub>i</sub>表示个人的幸福程度，解释变量DONA<sub>i</sub>表示个人是否有慈善捐赠行为。X<sub>i</sub>表示影响居民幸福感的一系列控制变量，μ表示随机误差项。</p>

        <h4>2. 中介效应模型</h4>
        <p>为检验慈善捐赠行为影响居民幸福感的影响机制，参考相关文献<sup>[53]</sup>，本文构建中介效应模型如下：</p>
        <p class="formula">M<sub>i</sub> = β<sub>0</sub> + β<sub>1</sub> DONA<sub>i</sub> + ΓX<sub>i</sub> + μ (2)</p>
        <p class="formula">Happiness<sub>i</sub> = γ<sub>0</sub> + γ<sub>1</sub> DONA<sub>i</sub> + γ<sub>2</sub> M<sub>i</sub> + ΓX<sub>i</sub> + μ (3)</p>
        <p>式（3）中，M<sub>i</sub>表示中介变量，具体包括社会关联能力、社会地位以及积极情绪。本文的中介效应检验流程如下：首先，对方程（1）进行基准回归，如果α<sub>1</sub>显著为正，说明慈善捐赠行为对居民幸福感有积极的影响；其次，若方程（2）系数β<sub>1</sub>和方程（3）系数γ<sub>2</sub>都显著为正，说明慈善捐赠行为可以通过中介变量M<sub>i</sub>作用于居民幸福感，在此基础上检验方程（3）γ<sub>1</sub>的显著性，若方程（3）γ<sub>1</sub>系数显著则说明存在部分中介效应，若方程（3）γ<sub>1</sub>不显著则说明存在完全中介效应。</p>

        <h3>（三）变量的选取与描述性统计</h3>
        <h4>1. 被解释变量：幸福感</h4>
        <p>幸福感是人们对总体生活情感态度的一种积极的心理评价<sup>[54]</sup>。学术界对于幸福感的评估采用多维度和单维度指标，其中在大规模调查中，对涉及跨时期和大样本的问题进行研究时，通常使用能够进行跨区域人际比较的单一指标<sup>[55]</sup>。中国家庭追踪调查属于跨时期和大样本调查，因此本文采用单维度幸福指标。本文幸福感指标来源于CFPS问卷中的问题"您觉得自己有多幸福感？"，受访者被要求在0分至10分中评分作答，0分代表幸福感最低，10分代表幸福感最强，数值越高代表居民幸福感越高。</p>

        <h4>2. 解释变量：慈善捐赠行为</h4>
        <p>本文慈善捐赠行为指标来源于CFPS问卷中的问题"请问您个人是否向任何组织或个人捐过款？"若受访者回答为"是"，则变量取值为1，"否"则为0。</p>

        <h4>3. 中介变量</h4>
        <p>（1）社会关联因素。本文选取两个指标进行衡量。其一，根据CFPS问卷中的问题"开朗善社交"，受访者被要求在1分至5分作答，1代表完全不符合，5代表完全符合，数值越高代表居民社交能力越强。其二，根据CFPS问卷中的问题"使用互联网络进行社交活动（如聊天、发微博等）的频率有多高？"对受访者的回答进行赋值。其中，从不=1，几个月一次=2，一月有一次=3，一月2～3次=4，一周1～2次=5，一周3～4次=6，几乎每天=1。数值越高表明居民进行互联网社交频率越高。</p>
        <p>（2）社会地位因素。本文根据CFPS问卷中的问题"您给自己在本地的社会地位打几分？"，受访者要求在1分至5分之间作答，1分代表很低，5分代表很高，数值越高代表居民自评主观社会地位越高。</p>
        <p>（3）积极情绪因素。个体的自信心在一定程度上反映了积极情绪，因此本文根据CFPS问卷中的问题"您给自己未来的信心程度打几分？"受访者要求在1分至5分之间作答，1代表没有信心，5代表很有信心，数值越高代表居民对未来的信心程度越高。</p>

        <h4>4. 控制变量</h4>
        <p>为了有效地衡量慈善捐赠行为对居民幸福感的影响，本文参照以往研究居民幸福感的选取方式<sup>[1]</sup>，选取人口学特征、就业因素、经济政策因素、社会网络因素、社会资本因素及自然环境因素作为影响幸福感的控制变量。人口学特征具体包括性别（女性=0；男性=1）、年龄及年龄的平方、受教育水平（区间为1～8，文盲/半文盲=1，小学=2，初中=3，高中=4，大专=5，大学本科=6，硕士=7，博士=8）、婚姻状况（其他=0，已婚=1）、健康状况（区间为1～7，很差=1，很好=7）、外貌特征（区间为1～7，很差=1，很好=7）和户口类型（农村=0，城镇=1）；就业因素选取当前就业状态（其他=0，就业=1）和收入满意度（区间为1～5，非常不满意=1，非常满意=5）；经济政策因素包括政府腐败（区间为0～10，不严重=0，非常严重=10），社会网络因素包括对陌生人的信任度（区间为0～10，常不信任=0，非常信任=10）和对本地政府官员的信任度（区间为0～10，常不信任=0，非常信任=10）；社会资本因素包括父亲的受教育水平（区间为1～8，文盲/半文盲=1，小学=2，初中=3，高中=4，大专=5，大学本科=6，硕士=7，博士=8）；考虑到居民所在省市的社会、经济、文化、自然环境对其幸福感可能也有影响，以及各省消费文化的不同，因此本文还控制了省级地区变量。</p>

