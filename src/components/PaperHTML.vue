        <p>前文的基准回归证明慈善捐赠行为对居民幸福感有显著且稳健的影响。那么慈善捐赠行为通过何种机制对居民幸福感产生影响？本节基于前文理论框架，使用中介效应回归方法对慈善捐赠行为提升居民幸福感的内在机制进行实证分析。</p>

