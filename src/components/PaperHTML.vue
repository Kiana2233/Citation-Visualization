    <section class="english-abstract">
      <h2>Abstract</h2>
      <p>In the context of high-quality development, people's longing for a better life is becoming stronger and stronger, and improving people's happiness is becoming a major issue of concern to society. Using the 2018 China Family Panel Studies (CFPS), this paper analyzes the influence and mechanism of individual charity donation behavior on their subjective happiness. The study found that charity donation behavior has a positive and robust effect on the happiness of residents. Mechanism analysis found that improving social association ability, social status, and increasing positive emotions is an important channel for charity donation behavior to affect the happiness of residents. The heterogeneous analysis found that the positive effect of charity donation behavior on the happiness of residents was more evident in high-income satisfaction groups, urban populations, low-education groups, middle-aged populations, and samples from northern and western regions. This study provides new ideas and insights for improving people's happiness and realizing people's longing for a better life.</p>
      <p class="keywords"><strong>Key words:</strong> charity donation behavior; happiness; social association ability; social status; positive emotions</p>
