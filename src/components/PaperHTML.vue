        <p>收入是决定个人是否具有慈善捐赠动机的重要因素，不同收入水平下，慈善捐赠行为可能对幸福感的应先存在差异。本文根据CFPS调查问卷中受访者对“您对这份工作的收入有多满意？”的回答，将收入划分为3个层次，即低收入满意、中等收入满意度和高收入满意，分别赋值为0、1和2。由表6第（1）列可知，低收入满意度与中等收入满意度的系数不显著，而高收入满意度的系数在1%水平上显著，系数符号为正，说明慈善捐赠行为更能提升高收入满意度样本的幸福感。对此可能的解释是，慈善捐赠行为对幸福感的影响存在“收入门槛效应”，当收入较低，即低收入满意度与中等收入满意度时，慈善捐赠行为挤占了个体用于其他需求的支出，降低了个体效用。而当收入较高，即对自己收入感到满意时，此时慈善捐赠行为提高了个体效用，进而有效提升了其幸福感。</p>

