        <p>基于社会关联视角的中介回归模型如表9所示。表9中列（1）和列（2）展示了用个体社交能力作为中介变量的回归结果。具体地，列（1）展示了基于式（2）的回归结果，慈善捐赠行为的系数为正，并且在1%的水平上显著，说明慈善捐赠行为显著提升了个体的社交能力。列（2）展示了基于式（3）的回归结果，社交能力的系数为正，且在1%的水平上显著，即慈善捐赠行为可以通过提升个体的社交能力进而提升其幸福感。表9中列（3）展示了基于式（2）的回归结果，慈善捐赠行为的系数为正，并且在1%的水平上显著，说明慈善捐赠行为显著增加了个体互联网社交的频率。列（4）展示了基于式（3）的回归结果，互联网社交频率的系数为正，且在5%的水平上显著，即慈善捐赠行为可以通过增加个体互联网社交频率进而提升其幸福感，说明社会关联是个体慈善捐赠行为影响其幸福感的中介因子。</p>
